function showChangeForm() {
    var changeNameForm = document.getElementById("changeNameForm");
    if (changeNameForm.style.display === "none") {
        changeNameForm.style.display = "block";
    } else {
        changeNameForm.style.display = "none";
    }
}
function showChangePasswordForm() {
    var paymentsForm = document.getElementById("changePasswordForm");
    if (paymentsForm.style.display === "none") {
        paymentsForm.style.display = "block";
    } else {
        paymentsForm.style.display = "none";
    }
}